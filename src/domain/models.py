from dataclasses import dataclass
from typing import Optional


@dataclass(frozen=True)
class AddressGeo:
    lat: str
    lng: str


@dataclass(frozen=True)
class Address:
    street: str
    suite: str
    city: str
    zipcode: str
    geo: AddressGeo


@dataclass(frozen=True)
class Company:
    name: str
    catchPhrase: str
    bs: str


@dataclass(frozen=True)
class User:
    id: int
    name: str
    username: str
    email: str
    phone: str
    website: str
    address: Address
    company: Company